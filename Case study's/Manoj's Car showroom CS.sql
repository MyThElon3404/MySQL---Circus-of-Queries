-- Q8. what is total revenue generated by the sales of 'SUV' cars in year 2022
select sum(cost_$) as total_revenue
from cars as c
inner join sales as s
	on c.car_id = s.car_id
where c.style = 'SUV' and YEAR(purchase_date) = 2022;

-- Q9. what is the name and city of the salesperson who sold most number of cars in 2023
with cte as (
	select top 1 
		salesman_id,
		COUNT(car_id) as total_sold_cars
	from sales
	group by salesman_id
	order by total_sold_cars desc
)
select s.name,
	s.city
from cte as c
inner join salespersons as s
	on c.salesman_id = s.salesman_id;

-- Q10. what is the name and city of the salesperson who generated highest revenue in year 2022
with cte as (
	select top 1 s.salesman_id,
		SUM(c.cost_$) as total_revenue
	from cars as c
	inner join sales as s
		on c.car_id = s.car_id
	group by s.salesman_id
	order by total_revenue desc
)
select s.name,
	s.city
from salespersons as s
inner join cte as c
	on s.salesman_id = c.salesman_id;

